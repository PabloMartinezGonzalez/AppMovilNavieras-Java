    private boolean nombre() {
        Pattern pat = Pattern.compile("^[a-zA-ZñÑáéíóúÁÉÍÓÚüÜ]{2,16}$");
        Matcher mat = pat.matcher(txtnombre.getText());

        return !mat.matches();
    }

    private boolean apellidos() {
        Pattern pat = Pattern.compile("^[a-zA-ZñÑáéíóúÁÉÍÓÚüÜ-]+([ '-][a-zA-ZZñÑáéíóúÁÉÍÓÚüÜ]+)*");
        Matcher mat = pat.matcher(txtapellidos.getText());

        return !mat.matches();
    }

    private boolean codPostal() {
        boolean estado = false;
        Pattern pat = Pattern.compile("^[0-9]{5}$");
        Matcher mat = pat.matcher(txtpostal.getText());

        return !mat.matches();
    }

    private boolean dni() {

        final String dniChars = "TRWAGMYFPDXBNJZSQVHLCKEabcdefghijklmnopqrstuvwxyz";
        String d = dni.getText();

        if (d.length() == 9 && !d.contains(" ")) {
            String intPartDNI = d.trim().substring(0, 7);
            String ltrDNI = "" + d.charAt(8);
            try {
                Integer.parseInt(intPartDNI);
            } catch (NumberFormatException excepcion) {
                return false;
            }

            return dniChars.contains(ltrDNI);
        } else {
            return false;
        }
    }

    private boolean correo() {

        Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher mather = pattern.matcher(correo.getText());
        return mather.find() == false;
    }
    private boolean telefono() {

        Pattern pattern = Pattern.compile("^(\\+34|0034|34)?[6789]\\d{8}$");
        Matcher mather = pattern.matcher(telefono.getText());
        return mather.find() == false;
    }

    private void comprobaciones() {
        boolean ok = true;
        if (nombre()) {
            falloNombre.setVisible(true);
            ok = false;
        } else {
            falloNombre.setVisible(false);
            
        }
        if (apellidos()) {
            falloapellidos.setVisible(true);
            ok = false;
        } else {
            falloapellidos.setVisible(false);
            
        }
        if (codPostal()) {
            falloPostal.setVisible(true);
            ok = false;
        } else {
            falloPostal.setVisible(false);
        }
        if (dni()) {
            fallodni.setVisible(false);

        } else {
            fallodni.setVisible(true);
            ok = false;
        }
        if (correo()) {
            fallocorreo.setVisible(true);
            ok = false;

        } else {
            fallocorreo.setVisible(false);
        }
        if (telefono()) {
            fallotelefono.setVisible(true);
            ok = false;

        } else {
            fallotelefono.setVisible(false);
        }
        if(ok){
            reserva.setNombre(txtnombre.getText()+" "+txtapellidos.getText());
            reserva.setCodPostal(txtpostal.getText());
            reserva.setDni(dni.getText());
            reserva.setCorreo(correo.getText());
            reserva.setTelefono(telefono.getText());
            fa.vistaPago(this,reserva);
            this.setVisible(false);
        }
    }

