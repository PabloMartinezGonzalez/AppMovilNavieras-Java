    public void cerrar() {
        this.dispose();
        VIndividual vi = new VIndividual();
        VGrupo vg = new VGrupo();
        if(this.parent.getClass().equals(vi.getClass())){
            vi = (VIndividual) parent;
            vi.cerrar();
        }else if(this.parent.getClass().equals(vg.getClass())){
            vg = (VGrupo) parent;
            vg.cerrar();
        }
    }

    public void confirmacionSalir(boolean dec) {
        if (dec) {
            this.cerrar();
        }
    }
