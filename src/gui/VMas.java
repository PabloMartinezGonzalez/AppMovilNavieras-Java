    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
        // TODO add your handling code here:
        
        if (correoText.getText().isEmpty()) 
            falloc.setVisible(true);
        else 
            falloc.setVisible(false);
        if (mensajeText.getText().isEmpty()) 
            fallom.setVisible(true);
         else 
            fallom.setVisible(false);
        
        if(!correoText.getText().isEmpty() && !mensajeText.getText().isEmpty()){
            Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
            Matcher mather = pattern.matcher(correoText.getText());
            if (mather.find() == false) {
                falloc.setVisible(true);
            } else {
                fa.muestraMensaje(this, "Mensaje enviado correctamente");
                mensajeText.setText("");
                correoText.setText("");
            }
        }  
    }//GEN-LAST:event_enviarActionPerformed


