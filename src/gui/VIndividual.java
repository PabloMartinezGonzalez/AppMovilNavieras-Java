    private void comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprarActionPerformed
        // TODO add your handling code here:
        if (numTotal > 0) {
            Integer numEntradasBb = Integer.parseInt(numBb.getText());
            Integer numEntradasNi = Integer.parseInt(numNi.getText());
            Integer numEntradasEs = Integer.parseInt(numEs.getText());
            if ((numEntradasBb + numEntradasNi) == numTotal) {
                fa.muestraMensaje(parent, "Los niños y bebes no pueden viajar solos.");
            } else if ((numEntradasBb + numEntradasNi + numEntradasEs) == numTotal) {
                fa.avisoEdad(this);
            } else {
                
                comprar();
            }
        } else {
            fa.muestraMensaje(parent, "Debe seleccionar al menos un billete.");
        }
    }//GEN-LAST:event_comprarActionPerformed

