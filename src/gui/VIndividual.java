    private void calculaTotal() {
        precioTotal = 0.0;
        numTotal = 0;
        double numEntradasOrd = Double.parseDouble(numOrd.getText());
        numTotal += numEntradasOrd;
        precioTotal = precioTotal + (precioOrd * numEntradasOrd);
        double numEntradasBb = Double.parseDouble(numBb.getText());
        numTotal += numEntradasBb;
        precioTotal = precioTotal + (precioBb * numEntradasBb);
        double numEntradasNi = Double.parseDouble(numNi.getText());
        numTotal += numEntradasNi;
        precioTotal = precioTotal + (precioNi * numEntradasNi);
        double numEntradasEs = Double.parseDouble(numEs.getText());
        numTotal += numEntradasEs;
        precioTotal = precioTotal + (precioEs * numEntradasEs);
        double numEntradasSe = Double.parseDouble(numSe.getText());
        numTotal += numEntradasSe;
        precioTotal = precioTotal + (precioSe * numEntradasSe);
        if (precioTotal == 0.0) {
            DecimalFormat df = new DecimalFormat("0.00");
            precTotal.setText(df.format(precioTotal) + "€");
        } else {
            DecimalFormat df = new DecimalFormat("#.00");
            precTotal.setText(df.format(precioTotal) + "€");
        }

    }


    private void numOrdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_numOrdFocusLost
        // TODO add your handling code here:
        try {
            Integer numEntradasOrd = Integer.parseInt(numOrd.getText());
            Integer numEntradasBb = Integer.parseInt(numBb.getText());
            Integer numEntradasNi = Integer.parseInt(numNi.getText());
            Integer numEntradasEs = Integer.parseInt(numEs.getText());
            Integer numEntradasSe = Integer.parseInt(numSe.getText());
            calculaTotal();
            numOrd.setText(numEntradasOrd.toString());
            if (numEntradasOrd < 0) {

                numOrd.setText("0");
                calculaTotal();
            }

            if (numTotal > 10) {
                numTotal -= numEntradasOrd;
                numEntradasOrd = 0;
                while (numTotal < 10) {
                    numTotal += 1;
                    numEntradasOrd += 1;
                }
                numOrd.setText(numEntradasOrd.toString());
                calculaTotal();
            }
        } catch (NumberFormatException excepcion) {
            numOrd.setText("0");
            calculaTotal();
        }
    }//GEN-LAST:event_numOrdFocusLost

